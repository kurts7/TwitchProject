        <h2 class="title">Аукцион Промо-блока</h2>
        <p>Каждое место в Промо-блоке выставляется на аукционе, в котором может принять участие любой участник системы «Twitch Master», прошедший модерацию описания профиля. Подробная информация о Промо-блоке и таблица стоимости мест в Промо-блоке приведена в соответствующем <a href="#promo-channel">разделе помощи</a>.</p>
        <p>Если до начала трансляции остается не менее <b>12 часов</b> и аукцион не был завершен досрочно, любой пользователь с достаточным количеством кредитов на счете может сделать ставку и тем самым стать участником аукциона.</p>
        <p>Размер минимальной ставки шага аукциона определяется по формуле:</p>
        <pre>Min_bet = Current_bet + Current_bet * 15 / 100</pre>
        <p>Где <i>Min_bet</i> — минимальный размер ставки, необходимой для участия в аукционе, а <i>Current_bet</i> — текущая ставка на выбранное место Промо-блока. Иными словами, каждая следующая ставка должна быть не менее чем на <b>15%</b> больше предыдущей.</p>
        <p>Если же ставок еще нет, то стоимость места в Промо-блоке определяется по обычной схеме, описанной в <a href="#promo-channel">таблице цен</a>, в зависимости от заблаговременности и продолжительности размещения.</p>
        <p>Любой аукцион может быть завершен досрочно. Для этого необходимо сделать ставку по, так называемой, блиц-цене, размер которой определяется следующей формулой:</p>
        <pre>Blitz_price = Base_price * 7</pre>
        <p>Где <i>Blitz_price</i> — блиц-цена, необходимая для досрочного выбора победителя и автоматического завершения аукциона, а <i>Base_price</i> — базовая цена выбранного места в Промо-блоке, согласно таблице цен (не текущая ставка).</p>
        <p>Победную ставку на место в Промо-блоке можно отменить не позднее чем за <b>12 часов</b> до завершения аукциона. Если ставку одного участника аукциона перебил другой участник, то ее отменить уже нельзя. При отмене ставки, на счет пользователя возвращается сумма кредитов, равная максимальной ставке в рамках выбранного аукциона, за вычетом комиссии — <b>45%</b>.</p>
        <p>Кредиты, задействованные на ставках, снимаются со счета пользователя <b>до планового завершения аукциона</b>, а в случае проигрыша, возвращаются обратно (обычно в течение часа) за вычетом комиссии — <b>5%</b> для обычных пользователей и <b>1%</b> для членов <a href="#vip-club">VIP-клуба</a>.</p>
