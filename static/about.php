        <h2 class="title">Коэффициент конвертации</h2>
        <p><b>Коэффициент конвертации</b> используется при начислении кредитов на счет участника системы, которые он заработал, просматривая трансляции других участников. По умолчанию этот коэффициент равен <b>1.66</b><span class="credits-icon"></span> в секунду (<b>99.6</b><span class="credits-icon"></span> в минуту). Кредиты за просмотр трансляции начисляются по истечении каждой минуты, а их сумма высчитывается при помощи простейшей формулы:</p>
        <pre>Credits_earned = Seconds * Coefficient</pre>
        Где <i>Credits_earned</i> — сумма заработанных за текущую сессию кредитов, <i>Seconds</i> — время текущей сессии в секундах, <i>Coefficient</i> — коэффициент конвертации, определенный для текущего пользователя.
